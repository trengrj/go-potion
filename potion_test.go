// testCacheDir resolves the shared model cache used by tests and benchmarks:
// the library default (os.UserCacheDir()/go-potion), overridable via
// GO_POTION_HOME
func testCacheDir(tb testing.TB) string {
	cacheDir, err := resolveCacheDir()
	if err != nil {
		tb.Fatalf("Failed to resolve cache dir: %v", err)
	}
	if err := os.MkdirAll(cacheDir, 0755); err != nil {
		tb.Fatalf("Failed to create cache dir: %v", err)
	}
	return cacheDir
}

type EmbeddingTestCase struct {
	Text      string    `json:"text"`
	Embedding []float32 `json:"embedding"`
	Tokens    []int     `json:"tokens"`
}

func TestLoadAndEncode(t *testing.T) {
	// Reference samples are generated by validation/generate_tests.py using
	// the Python model2vec library
	for _, model := range Models() {
		t.Run(string(model), func(t *testing.T) {
			encoder, err := New(t.Context(), model)
			if err != nil {
				t.Fatalf("Failed to create encoder: %v", err)
			}

			samplesPath := filepath.Join("validation", "samples", fmt.Sprintf("%s.json", model))
			data, err := os.ReadFile(samplesPath)
			if err != nil {
				t.Fatalf("Failed to read %s: %v", samplesPath, err)
			}

			var testCases []EmbeddingTestCase
			if err := json.Unmarshal(data, &testCases); err != nil {
				t.Fatalf("Failed to parse %s: %v", samplesPath, err)
			}

			for _, tc := range testCases {
				t.Run(tc.Text, func(t *testing.T) {
					tokens, err := encoder.Tokenize(tc.Text)
					if err != nil {
						t.Fatalf("Failed to tokenize: %v", err)
					}

					if len(tokens) != len(tc.Tokens) {
						t.Errorf("Expected %d tokens, got %d", len(tc.Tokens), len(tokens))
						t.Errorf("Expected: %v", tc.Tokens)
						t.Errorf("Got: %v", tokens)
						return
					}

					for i, token := range tokens {
						if token != tc.Tokens[i] {
							t.Errorf("Token mismatch at position %d: expected %d, got %d", i, tc.Tokens[i], token)
						}
					}

					embedding, err := encoder.Encode(tc.Text)
					if err != nil {
						t.Fatalf("Failed to encode: %v", err)
					}

					if len(embedding) != len(tc.Embedding) {
						t.Errorf("Expected embedding length %d, got %d", len(tc.Embedding), len(embedding))
					}
					if len(embedding) != encoder.Dimensions() {
						t.Errorf("Dimensions() = %d, but embedding length is %d", encoder.Dimensions(), len(embedding))
					}

					// Compare with a small tolerance due to floating point differences
					const tolerance = 1e-6
					for j := range tc.Embedding {
						if abs(embedding[j]-tc.Embedding[j]) > tolerance {
							t.Errorf("At index %d: expected %f, got %f", j, tc.Embedding[j], embedding[j])
						}
					}
				})
			}
		})
	}
}

